public class Day003 {
    public static void main(String[] args) {
        // Literal : Any constant value which can be assinged to a variable is called
        // literal/constant.

        /*
         * 101 -> integer literal
         * 10.1 -> double literal (defult type for decimals)
         * 10.1f -> float literal
         * 'A' -> character literal
         * true -> boolean literal
         * "Harry" -> String literal
         * 56666666L -> long literal
         */

        String str = "Vishwas";
        // NOTE THAT THE S OF STRING SHOULD ALWAYS BE CAPITAL
        System.out.println(str);
    }
}
package Day_045_Is_Multiple_Inheritance_Allowed_In_Java;

// Multiple inheritance is when one class has more than one parent class.
// Java doesn't support multiple inheritance in classes, only in interfaces. This means a class can implement more than
// one interface but can only extend one parent class.
// Python , C++, and many other languages have multiple inheritance. The creators of Java deliberately made this choice
// because of the diamond problem ( to get clear understanding watch this video :
// https://www.youtube.com/watch?v=1-JBFJ8Xar0&ab_channel=CodingwithJohn )
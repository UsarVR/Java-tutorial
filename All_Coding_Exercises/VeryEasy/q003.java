// Create a function that takes an integer as input, increments it by 1 and returns the result.

package All_Coding_Exercises.VeryEasy;

public class q003 {
    static int increment(int number){
        return number+1;
    }
    public static void main(String[] args) {
        System.out.println("The number that comes after 123 is : "+increment(123));
    }
}

// OOPs Terminology

// 1. ABSTRACTION -> Hiding internal details (show only essential info)
// For example : using a phone without bothering how it was made

// 2. ENCAPSULATION -> The act of putting various components together (in a
// capsule)
// For example : laptop is a single entity with wifi + speaker + display +
// storage in a single box
/*
 * In Java encapsulation basically means that sensitive data can be hidden from
 * the users.
 */

// 3. INHERITANCE -> The act of deriving new things from existing things.
// For example : Rickshaw -> E-Rickshaw, Phone -> Smart Phone

// 4. POLYMORPHISM -> One entity many forms
// Smartphone -> Phone , Smartphone -> Calculator